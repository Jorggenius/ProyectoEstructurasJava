    private void llenarTablaRedenciones(){
        DefaultTableModel model = new DefaultTableModel(new String[]{"Id","Viaje","Fecha","Cliente"},controlHR.obtenerRedenciones(cliente).size());
        tablaRedenciones.setModel(model);
        TableModel modelR = tablaRedenciones.getModel();
        for (int i = 0; i < controlHR.obtenerRedenciones(cliente).size(); i++) {
            Redencion reden = controlHR.obtenerRedenciones(cliente).get(i);
            modelR.setValueAt(reden.getId(), i, 0);
            modelR.setValueAt(reden.getViaje(), i, 1);
            modelR.setValueAt(reden.getFechaCompra(), i, 2);
            modelR.setValueAt(reden.getCliente(), i, 3);
        }
    }
