    private void llenarTablaDevoluciones() {

        DefaultTableModel model = new DefaultTableModel(new String[]{"Id", "Viaje",
            "FechaReserva", "Cliente"},
                controlHD.obtenerDevoluciones(cliente).size());
        tablaDevoluciones.setModel(model);
        TableModel modelDevolucion = tablaDevoluciones.getModel();
        for (int i = 0; i < controlHD.obtenerDevoluciones(cliente).size(); i++) {
            Devolucion devolucion = controlHD.obtenerDevoluciones(cliente).get(i);
            modelDevolucion.setValueAt(devolucion.getId(), i, 0);
            modelDevolucion.setValueAt(devolucion.getViaje(), i, 1);
            modelDevolucion.setValueAt(devolucion.getFechaCompra(), i, 2);
            modelDevolucion.setValueAt(devolucion.getCliente(), i, 3);
        }

    }

