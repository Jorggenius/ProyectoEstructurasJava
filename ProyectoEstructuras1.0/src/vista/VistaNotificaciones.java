    private void llenarTablaNotifs() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"Cliente",
            "Fecha", "Empresa","Id","Tipo"}, controlN.getCliente()
                .getNotificaciones().size());
        tablaNotificaciones.setModel(model);
        TableModel modelNotif = tablaNotificaciones.getModel();
        for (int i = 0; i < controlN.getCliente().getNotificaciones().size(); i++) {
            Notificacion notificacion = controlN.getCliente().getNotificaciones().get(i);
            modelNotif.setValueAt(notificacion.getCliente(), i, 0);
            modelNotif.setValueAt(notificacion.getFecha(), i, 1);
            modelNotif.setValueAt(notificacion.getEmpresa(), i, 2);
            modelNotif.setValueAt(notificacion.getId(), i, 3);
            modelNotif.setValueAt(notificacion.getTipo(), i, 4);
        }
    }

    private void listenerTablaNotifs(){
        ListSelectionModel model = tablaNotificaciones.getSelectionModel();
        model.addListSelectionListener(new ListSelectionListener(){
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(tablaNotificaciones.getSelectedRow()!= -1){
                    int id = (Integer) tablaNotificaciones.getValueAt(tablaNotificaciones.getSelectedRow(), 3);
                    areaTxtNotifs.setText(controlN.conseguirNotif(id).getMensaje());
                }
            }
        });
    }
